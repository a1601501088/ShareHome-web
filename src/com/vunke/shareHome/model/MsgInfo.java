package com.vunke.shareHome.model;

public class MsgInfo {


	private String msg_id  ;
	private String user_id ;
	private String mobile_num ;
	private String create_time ;
	private String random_num ;
	private String if_alive ;
	private String update_time ;

	public String getMsg_id() {
		return msg_id;
	}
	public void setMsg_id(String msg_id) {
		this.msg_id = msg_id;
	}
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public String getMobile_num() {
		return mobile_num;
	}
	public void setMobile_num(String mobile_num) {
		this.mobile_num = mobile_num;
	}
	public String getCreate_time() {
		return create_time;
	}
	public void setCreate_time(String create_time) {
		this.create_time = create_time;
	}
	public String getRandom_num() {
		return random_num;
	}
	public void setRandom_num(String random_num) {
		this.random_num = random_num;
	}
	public String getIf_alive() {
		return if_alive;
	}
	public void setIf_alive(String if_alive) {
		this.if_alive = if_alive;
	}
	public String getUpdate_time() {
		return update_time;
	}
	public void setUpdate_time(String update_time) {
		this.update_time = update_time;
	}

	
}
